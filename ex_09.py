# Here's some new strange stuff, remember type it exactly

days = "Mon Tue Wed Thu Fri Sat Sun"
# The option \n means a new line
months = "Jan\nFeb\nMar\nApr\nMay\nJun\nJul\nAug"

print("Here are the days:", days)
print("Here are the months:", months)

# When we use """ (three double-quotes) means a free text like they represent.
# For this case, we can write a multi-line docstring.
# This three quotes usually will be used for docstrings like i say before,
# but this model can be used for all docstrings, because is more easy to understand the code
print("""
    There's something going on here.
    With the three double-quotes.
    We'll be able to type as much as we like.
    Even 4 lines if we want, or 5, or 6.
""")
